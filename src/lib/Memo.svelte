<script lang="ts">
	import { getContext } from "svelte";
  import { supabase } from "./db";
	import type { MemoResponse } from "./schema";
	import type { User } from "@supabase/supabase-js";

  export let memo: MemoResponse;
  export let onDelete = () => {};

	// context から user store を取得します
	const user = getContext<User>('user');

  let isPinned = memo.is_pinned;

  const toggle = async () => {
    try {
      const { data, error } = await supabase
        .from("memos")
        .update({ user_id: $user.id, is_pinned: !isPinned })
        .eq("id", memo.id)
        .select("is_pinned")
        .single();
      if (error) {
        throw error;
      }
      isPinned = data.is_pinned;
    } catch (error) {
      console.log("error", error);
    }
  };
</script>

<li
  class="w-full block cursor-pointer hover:bg-gray-200 focus:outline-none focus:bg-gray-200 transition duration-150 ease-in-out"
>
  <div class="flex items-center px-4 py-4 sm:px-6">
    <div class="min-w-0 flex-1 flex items-center">
      <div class="text-sm leading-5 font-medium truncate">{memo.text}</div>
    </div>
    <div>
      <input
        class="cursor-pointer"
        type="checkbox"
        on:change={toggle}
        bind:checked={isPinned}
      />
    </div>
    <button
      on:click={onDelete}
      class="w-4 h-4 ml-2 border-2 hover:border-black rounded"
    >
      <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20" fill="gray">
        <path
          fill-rule="evenodd"
          d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z"
          clip-rule="evenodd"
        />
      </svg>
    </button>
  </div>
</li>
